def shortest_path(source, target):
    """
    Returns the shortest list of (movie_id, person_id) pairs
    that connect the source to the target.

    If no possible path, returns None.
    """
    
    # nous avons la posssibilite de choisir en l'algo de recherche bfs et dfs
    
    n = input(" saisir le nombre 1 pour BFS et le nombre 2 DFS : ")
    start = Node(state=source, parent=None, action=None)
    if(n==1):
        # Queue pour BFS
        frontier = QueueFrontier()
        
    else:
        # Stack pour DFS
        frontier = StackFrontier()
        
    
    frontier.add(start)
    noeud_explorer = set()
    

    

    # parent_dictionary = {}
    while True:     
        
        if frontier.empty():
            raise Exception("no solution")

        # Choisissez un nœud dans la frontière
        node = frontier.remove()

        # Si le nœud est est celui qu'on cherche, alors nous avons une solution
        if node.state == target:
            # List to store shortest path
            shortest_path_list = []
            while node.parent is not None:
                shortest_path_list.append((node.action, node.state))
                node = node.parent
            shortest_path_list.reverse()
            return shortest_path_list

        # Marquer le nœud comme exploré
        noeud_explorer.add(node.state)
        

        # Ajouter les voisins à la frontière
        for action, state in neighbors_for_person(node.state):
            if not frontier.contains_state(state) and state not in noeud_explorer:
                new = Node(state=state, parent=node, action=action)
                frontier.add(new)
    raise NotImplementedError


    

def person_id_for_name(name):
    """
    Returns the IMDB id for a person's name,
    resolving ambiguities as needed.
    """
    person_ids = list(names.get(name.lower(), set()))
    if len(person_ids) == 0:
        return None
    elif len(person_ids) > 1:
        print(f"Which '{name}'?")
        for person_id in person_ids:
            person = people[person_id]
            name = person["name"]
            birth = person["birth"]
            print(f"ID: {person_id}, Name: {name}, Birth: {birth}")
        try:
            person_id = input("Intended Person ID: ")
            if person_id in person_ids:
                return person_id
        except ValueError:
            pass
        return None
    else:
        return person_ids[0]


def neighbors_for_person(person_id):
    """
    Returns (movie_id, person_id) pairs for people
    who starred with a given person.
    """
    movie_ids = people[person_id]["movies"]
    neighbors = set()
    for movie_id in movie_ids:
        for person_id in movies[movie_id]["stars"]:
            neighbors.add((movie_id, person_id))
    return neighbors


